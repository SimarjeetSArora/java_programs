class RelationalOp
{
	public static void main(String args[])
	{
		int a=40;
		int b=10;
		System.out.println("a==b: "+(a==b));
		System.out.println("a!=b: "+(a!=b));
		System.out.println("a>b: "+(a>b));
		System.out.println("a<b: "+(a<b));
		System.out.println("a<=b: "+(a<=b));
		System.out.println("a>=b: "+(a>=b));
	}
}